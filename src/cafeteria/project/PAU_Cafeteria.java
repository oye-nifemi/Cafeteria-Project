    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            int[] the_prices = {520, 150, 150, 200, 850, 280, 280, 280, 150, 280, 280, 250, 200};
            int index = jList1.getSelectedIndex();
            int money = the_prices[index];
            int quantity = 1;
            
            if (jLabel3.getText() == null){
                JOptionPane.showMessageDialog(rootPane, "Select an item from the menu.");
            }else if (jLabel3.getText() == "--"){
                JOptionPane.showMessageDialog(rootPane, "Select an item from the menu.");
            }

            if(jTextField1.getText() != ""){
                quantity = Integer.parseInt(jTextField1.getText());
                int total = quantity * money;
                jLabel3.setText(String.valueOf(total));
            }else{
                int total = quantity * money;
                jLabel3.setText(String.valueOf(total));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Invalid Entry");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

